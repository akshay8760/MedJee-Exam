        <h3>1. First Law of thermodynamics, ΔQ = ΔU + Δ W</h3><br>
        <h3>2. Work done –</h3><br>
        <ul style="list-style:none">
        <li>a. In an isothermal and reversible process =PΔV</li><br>
        <li>b. In an isothermal and irreversible process –</li><br>
        <p>W = 2.303nRT log V2/V1 <br><br>
                    or <br><br>
             W = 2.303nRT log P1/P2 </p><br>
        <li>c.	Work done in an adiabatic process i.e PVγ = constant &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp(where γ = Cp/Cv)</li><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspW= ( P1V1-P2V2 ) / ( γ-1 ) = ( nR ( T1-T2 ) ) / ( γ-1 )</p>
        </ul><br>

        <h3>3. Adiabatic process can be represented by :-</h3><br>
        <ul style="list-style:none">
        <li>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspa. PV <sup>γ</sup>  = constant </li><br>
        <li>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspb. TV<sup>γ-1 </sup> = constant</li><br>
        <li>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspc. P<sup>1-r</sup>Tr = constant</li><br>
        </ul><br>

        <h3>4. Work done in isothermal expansion > Adiabatic expansion.</h3><br>
        <h3>5. Change in Internal energy is represented by :-</h3><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspΔU = nCvΔT = P2V2 –P1V1 / r-1   =   f / 2nRΔT = f / 2 ( P2V2- P1V1 ) <br><br>	( where f is degree of freedom which is 3 for monatomic, 5 for                           diatomic and 6 for polyatomic)</p><br>

        <h3>6. Specific heat constant at const. Volume (Cv) = R / ( γ-1 )</h3><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAt constant pressure (Cp) =γR / ( γ-1 )</p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspWhere γ = 1+2/f  = Cp/Cv</p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAnd Cp-Cv = R</p><br>

        <h3>7.  Heat energy supplied at constant pressure to monoatomic / diatomic gas. The part of this heat that  goes to increase it’s internal energy is given by</h3>
        <br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspΔU/ΔQ  = nCvΔT/nCpΔT  =Cv/Cp  =1/γ</p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAnd part of this heat that goes to work done is given by-</p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspΔW/ΔQ = (ΔQ-ΔU)/ΔQ  = (nCpΔT-nCvΔT)/nCpΔT  =  (Cp-Cv)/Cp   = 1- 1/γ</p><br>

        <h3>8. Slope of adiabatic = γ × slope of isotherm</h3><br>

        <h3>9. For polytropic Process</h3><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspPV<sup>N</sup> = constant </p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspW = P2V2 – P1V1 / 1-N</p><br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsSpecific Heat, C = Cv + R/(1-N)</p><br>

